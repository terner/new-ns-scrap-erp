create table public.customer_advance_statuses (
  id bigint generated by default as identity primary key,
  code text not null,
  name text not null,
  sort_order integer not null,
  is_initial boolean not null default false,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint customer_advance_statuses_code_key unique (code),
  constraint customer_advance_statuses_sort_order_key unique (sort_order),
  constraint customer_advance_statuses_code_check check (code = lower(code) and code ~ '^[a-z_]+$')
);

create unique index customer_advance_statuses_one_initial
  on public.customer_advance_statuses (is_initial)
  where is_initial;

