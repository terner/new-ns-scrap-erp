create table if not exists public.production_order_status_logs (
  event_key text not null default ('POSTATUS-'::text || gen_random_uuid()::text),
  action text not null,
  order_doc_no text not null,
  from_status text null,
  to_status text not null,
  note text null,
  meta jsonb null,
  created_at timestamptz not null default now(),
  created_by text null,
  id bigint generated by default as identity primary key,
  order_id bigint not null references public.production_orders(id) on delete restrict
);

create unique index if not exists idx_production_order_status_logs_event_key
  on public.production_order_status_logs(event_key);

create index if not exists idx_production_order_status_logs_order_created
  on public.production_order_status_logs(order_id, created_at desc);

create index if not exists idx_production_order_status_logs_doc_no
  on public.production_order_status_logs(order_doc_no);

create index if not exists idx_production_order_status_logs_action
  on public.production_order_status_logs(action);

alter table public.production_order_status_logs enable row level security;

alter table public.production_inputs
