  add column if not exists reversal_doc_no text;

alter table public.production_outputs
  add column if not exists doc_no text,
  add column if not exists status text not null default 'active',
  add column if not exists category_code text,
  add column if not exists destination_warehouse_id bigint references public.warehouses(id) on update no action on delete no action,
  add column if not exists lot_no text,
  add column if not exists source_wip_qty numeric,
  add column if not exists reversed_at timestamptz,
  add column if not exists reversed_by text,
  add column if not exists reverse_reason text,
  add column if not exists reversal_doc_no text;

with numbered_inputs as (
  select
    pi.id,
    'PI' || to_char(pi.date, 'YYMM') || '-L' || lpad(row_number() over (partition by to_char(pi.date, 'YYMM') order by pi.date, pi.id)::text, 5, '0') as generated_doc_no
  from public.production_inputs pi
  where pi.doc_no is null
)
update public.production_inputs pi
set doc_no = numbered_inputs.generated_doc_no
from numbered_inputs
where pi.id = numbered_inputs.id;

update public.production_inputs pi
set
  source_warehouse_id = coalesce(pi.source_warehouse_id, po.warehouse_from_id, po.warehouse_id),
  wip_warehouse_id = coalesce(pi.wip_warehouse_id, po.warehouse_wip_id),
  wac_unit_cost = coalesce(pi.wac_unit_cost, pi.unit_cost)
from public.production_orders po
where pi.order_id = po.id;

with numbered_outputs as (
  select
    po2.id,
    'PO2' || to_char(po2.date, 'YYMM') || '-L' || lpad(row_number() over (partition by to_char(po2.date, 'YYMM') order by po2.date, po2.id)::text, 5, '0') as generated_doc_no
  from public.production_outputs po2
  where po2.doc_no is null
)
update public.production_outputs po2
set doc_no = numbered_outputs.generated_doc_no
from numbered_outputs
where po2.id = numbered_outputs.id;

update public.production_outputs po2
set category_code = poc.code
from public.production_output_categories poc
where po2.output_category = poc.id
  and po2.category_code is null;

update public.production_outputs po2
set
  destination_warehouse_id = coalesce(po2.destination_warehouse_id, po.warehouse_to_id, po.warehouse_id),
  source_wip_qty = coalesce(po2.source_wip_qty, po2.qty)
from public.production_orders po
where po2.order_id = po.id;

