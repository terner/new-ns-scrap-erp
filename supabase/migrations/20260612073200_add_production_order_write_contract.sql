do $$
begin
  if not exists (select 1 from pg_constraint where conname = 'production_inputs_status_check') then
    alter table public.production_inputs
      add constraint production_inputs_status_check
      check (status in ('active', 'reversed'));
  end if;

  if not exists (select 1 from pg_constraint where conname = 'production_outputs_status_check') then
    alter table public.production_outputs
      add constraint production_outputs_status_check
      check (status in ('active', 'reversed'));
  end if;

  if not exists (select 1 from pg_constraint where conname = 'production_outputs_category_code_check') then
    alter table public.production_outputs
      add constraint production_outputs_category_code_check
      check (category_code is null or category_code in ('FG', 'RM', 'LOSS', 'CUSTOMER_RETURN'));
  end if;
end;
$$;

with created_events as (
  select
    po.id as order_id,
    po.doc_no as order_doc_no,
    'created'::text as action,
    null::text as from_status,
    coalesce(po.status, 'Open') as to_status,
    coalesce(po.created_at, now()) as created_at,
    nullif(po.updated_by, '') as created_by,
    jsonb_build_object('source', 'migration_backfill') as meta
  from public.production_orders po
  where not exists (
    select 1
    from public.production_order_status_logs existing
    where existing.order_id = po.id
  )
),
numbered_events as (
  select
    created_events.*,
    row_number() over (partition by order_id order by created_at, action) as seq
  from created_events
)
insert into public.production_order_status_logs (
  event_key,
  action,
  order_doc_no,
  from_status,
  to_status,
  meta,
  created_at,
  created_by,
  order_id
)
select
  'POSTATUS-' || order_doc_no || '-' || lpad(seq::text, 4, '0') as event_key,
  action,
  order_doc_no,
  from_status,
  to_status,
  meta,
  created_at,
  created_by,
  order_id
from numbered_events
on conflict (event_key) do nothing;
